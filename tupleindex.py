tuple = (4 , "Python" , 15.5 , 9 , 3.14)
print("Tuple:", tuple)

print("First element:", tuple[0])
print("Last element:", tuple[-1])
print(end = "\n")

print("Slice from index 1 to 3:", tuple[1:4])
print("Slice from index 2 to the end:", tuple[2:])
print(end = "\n")

print("Slice from the start to index 3:", tuple[:4])
print("Slice from index 1 to 4 with step 2:", tuple[1:5:2])
print(end = "\n")


